package it.polimi.ingsw;

public class IntegerPair {
    private final Integer x;
    private final Integer y;

    public IntegerPair(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

}
